package March2022;

public class Calcultion {

	public static void main(String[] args) {
		
		System.out.println(moneyAddition(2,3));
		System.out.println(moneysubtraction(2,3));
	}
	
	
	static double moneyAddition(double value1, double value2) {
		
		
		return value1+value2;
	}
	
static double moneysubtraction(double value1, double value2) {
		
		
		return value1-value2;
	}

}
