package March2022;

public class ClassAddSubtMult {
	
	
static int AddingNumberOfGirls(int NumGirls1,int NumGirls2) {
	return NumGirls1+NumGirls2;
}	

static int SubtractNumberOfGirls(int NumGirls1,int NumGirls2) {	
return NumGirls1-NumGirls2;
}
	
static int MultiplicationNumberOfGirls(int NumGirls1,int NumGirls2) {
	return NumGirls1*NumGirls2;
	}
}
