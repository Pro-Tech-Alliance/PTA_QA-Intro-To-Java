package AssignmentApril1;

public class MultiplicationFunction {

	int multiplyTwoNumbers(int pta1, int pta2) {
		return pta1*pta2;
		
	}
	
	
	int multiplyFourNumbers(int pro1, int pro2, int pro3, int pro4) {
		
		return pro1*pro2*pro3*pro4;
	}
	
	int multiplyFiveNumbers(int practice1, int practice2, int practice3, int practice4, int practice5) {
		
		return practice1*practice2*practice3*practice4*practice5;
}


	int multiplySixNumbers(int good1, int good2, int good3, int good4, int good5, int good6) {
		
		return good1*good2*good3*good4*good5*good6;
	}
}


