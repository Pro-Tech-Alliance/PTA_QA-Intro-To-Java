package AssignmentApril1;

public class SubtractionFunction {
	
	int subTwoNumbers(int april1, int april2) {
		return april1-april2;
		
	}
	
	
	int subFourNumbers(int april1, int april2, int april3, int april4) {
		
		return april1-april2-april3-april4;
	}
	
	int subFiveNumbers(int april1, int april2, int april3, int april4, int april5) {
		
		return april1-april2-april3-april4-april5;
}


	}




