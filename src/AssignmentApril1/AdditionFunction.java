package AssignmentApril1;

public class AdditionFunction {

	int addTwoNumbers(int ball1, int ball2) {
		return ball1+ball2;
		
	}
	
	
	int addFourNumber(int ball1, int ball2, int ball3, int ball4) {
		
		return ball1+ball2+ball3+ball4;
	}
	
	int addFiveNumber(int bet1, int bet2, int bet3, int bet4, int bet5) {
		
		return bet1+bet2+bet3+bet4+bet5;
}


	int addSixNumber(int number1, int number2, int number3, int number4, int number5, int number6) {
		
		return number1+number2+number3+number4+number5+number6;
	}
}

	

	
	

