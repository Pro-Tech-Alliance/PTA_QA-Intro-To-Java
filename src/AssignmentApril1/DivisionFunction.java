package AssignmentApril1;

public class DivisionFunction {
	
	int dividTwoNumbers(int sch1, int sch2) {
		return sch1/sch2;
		
	}
	
	
	int dividFourNumbers(int sch1, int sch2, int sch3, int sch4) {
		
		return sch1/sch2/sch3/sch4;
	}
	
	int dividFiveNumbers(int sch1, int sch2, int sch3, int sch4, int sch5) {
		
		return sch1/sch2/sch3/sch4/sch5;
}


	int dividSixNumbers(int number1, int number2, int number3, int number4, int number5, int number6) {
		
		return number1/number2/number3/number4/number5/number6;
	}
}



